def add(a, b):
    return a + b

def subtract(a, b):
    return a - b

def multiply(a, b):
    return a * b

def divide(a, b):
    return a / b

def power(a, b):
    return a ** b

def modulo(a, b):
    return a % b

def square_root(a):
    return a ** 0.5

def logarithm(a, b):
    import math
    return math.log(a, b)

def sine(a):
    import math
    return math.sin(a)

def cosine(a):
    import math
    return math.cos(a)

def tangent(a):
    import math
    return math.tan(a)


